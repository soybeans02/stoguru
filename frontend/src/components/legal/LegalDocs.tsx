export type LegalDocType = 'privacy' | 'terms' | 'cookie' | 'commerce';

const TITLES: Record<LegalDocType, string> = {
  privacy: 'プライバシーポリシー',
  terms: '利用規約',
  cookie: 'クッキーポリシー',
  commerce: '特定商取引法に基づく表記',
};

export function LegalSheet({ doc, onClose }: { doc: LegalDocType; onClose: () => void }) {
  return (
    <Sheet isOpen onClose={onClose} title={TITLES[doc]} maxWidth="lg">
      <div className="legal-doc">
        {doc === 'privacy' && <PrivacyPolicy />}
        {doc === 'terms' && <TermsOfService />}
        {doc === 'cookie' && <CookiePolicy />}
        {doc === 'commerce' && <Commerce />}
      </div>
      <style>{`
        .legal-doc { font-size: 13px; line-height: 1.75; color: var(--text-primary); }
        .legal-doc h2 { font-size: 15px; font-weight: 700; margin: 22px 0 8px; color: var(--text-primary); }
        .legal-doc h2:first-child { margin-top: 0; }
        .legal-doc h3 { font-size: 13.5px; font-weight: 600; margin: 14px 0 6px; color: var(--text-primary); }
        .legal-doc p { margin: 0 0 10px; color: var(--text-secondary); }
        .legal-doc ul, .legal-doc ol { padding-left: 1.4em; margin: 0 0 10px; color: var(--text-secondary); }
        .legal-doc li { margin: 3px 0; }
        .legal-doc strong { color: var(--text-primary); font-weight: 600; }
        .legal-doc .meta { font-size: 11.5px; color: var(--text-tertiary); margin-bottom: 18px; }
        .legal-doc dl { display: grid; grid-template-columns: 130px 1fr; gap: 6px 14px; margin: 0 0 10px; }
        .legal-doc dt { font-weight: 600; color: var(--text-primary); }
        .legal-doc dd { margin: 0; color: var(--text-secondary); }
        @media (max-width: 480px) {
          .legal-doc dl { grid-template-columns: 1fr; gap: 2px; }
          .legal-doc dt { margin-top: 8px; }
        }
      `}</style>
    </Sheet>
  );
}

